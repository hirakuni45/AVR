#include "task.hpp"
#include "twi.hpp"
#include "time.h"

namespace app {

	//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
	/*!
		@brief	キッチン・タイマー・クラス
	*/
	//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
	class timer : public i_task {
		task&	task_;

		struct mode {
			enum type {
				time,			///< 時間表示
				date,			///< 日付表示
				setting,		///< 時間設定
			};
		};

		mode::type	mode_;

		int32_t		speed_;
		int32_t		pos_;

		int8_t		page_;
		uint16_t	frame_;
		bool		dsp_date_;

		time_t		time_;

		device::twi	twi_;

		uint32_t get_time_();
		void draw_date_(int16_t ofs, const tm* t);
		void draw_time_(int16_t ofs, const tm* t);
	public:
		//-----------------------------------------------------------------//
		/*!
			@brief	コンストラクター
		*/
		//-----------------------------------------------------------------//
		timer(task& t) : task_(t), mode_(mode::time),
			speed_(0), pos_(0), page_(0), frame_(0), dsp_date_(true),
			time_(0) { }


		//-----------------------------------------------------------------//
		/*!
			@brief	デストラクター
		*/
		//-----------------------------------------------------------------//
		virtual ~timer() { }


		//-----------------------------------------------------------------//
		/*!
			@brief	初期化
		*/
		//-----------------------------------------------------------------//
		void init();


		//-----------------------------------------------------------------//
		/*!
			@brief	サービス
		*/
		//-----------------------------------------------------------------//
		void service();


		//-----------------------------------------------------------------//
		/*!
			@brief	廃棄
		*/
		//-----------------------------------------------------------------//
		void destroy();
	};
}
