namespace app {

	//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
	/*!
		@breif	タスクのインターフェース・クラス
	*/
	//+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++//
	struct i_task {

		//-----------------------------------------------------------------//
		/*!
			@breif	デストラクター
		*/
		//-----------------------------------------------------------------//
		virtual ~i_task() { }


		//-----------------------------------------------------------------//
		/*!
			@breif	初期化
		*/
		//-----------------------------------------------------------------//
		virtual void init() = 0;


		//-----------------------------------------------------------------//
		/*!
			@breif	サービス
		*/
		//-----------------------------------------------------------------//
		virtual void service() = 0;


		//-----------------------------------------------------------------//
		/*!
			@breif	廃棄
		*/
		//-----------------------------------------------------------------//
		virtual void destroy() = 0;
	};
}
